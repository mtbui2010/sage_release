def _has_word(word: str, text: str, *, case_sensitive: bool) -> bool:
    flags = 0 if case_sensitive else re.IGNORECASE
    pat = rf"(?:^|[^A-Za-z0-9]){re.escape(word)}(?:$|[^A-Za-z0-9])"
    return bool(re.search(pat, text, flags))


def _bib_key(authors: str, year: str, title: str) -> str:
    surname = re.split(r"\s+", authors.strip())[0].lower()
    surname = re.sub(r"[^a-z]", "", surname)
    first_word = re.findall(r"[a-z]{3,}", title.lower())
    tag = first_word[0] if first_word else "ref"
    return f"{surname}{year}{tag}"


def _infer_type(description: str) -> str:
    for v in _VENUE_INPROC_ACRONYMS:
        if _has_word(v, description, case_sensitive=True):
            return "inproceedings"
    for v in _VENUE_JOURNAL_ACRONYMS:
        if _has_word(v, description, case_sensitive=True):
            return "article"
    for v in _VENUE_JOURNAL_PHRASES:
        if v in description.lower():
            return "article"
    return "misc"


def _venue_from_desc(description: str) -> str:
    for v in _VENUE_INPROC_ACRONYMS + _VENUE_JOURNAL_ACRONYMS:
        if _has_word(v, description, case_sensitive=True):
            return v
    if "arxiv" in description.lower():
        return "arXiv preprint"
    return ""


def _latex_escape(s: str) -> str:
    return (s.replace("\\", r"\textbackslash{}")
             .replace("&",  r"\&")
             .replace("%",  r"\%")
             .replace("$",  r"\$")
             .replace("#",  r"\#")
             .replace("_",  r"\_"))


def _bibtex_entry(etype: str, key: str, fields: dict) -> str:
    lines = [f"@{etype}{{{key},"]
    for k, v in fields.items():
        if not v:
            continue
        v_str = str(v).strip() if k == "url" else _latex_escape(str(v).strip())
        lines.append(f"  {k:9s} = {{{v_str}}},")
    lines[-1] = lines[-1].rstrip(",")
    lines.append("}")
    return "\n".join(lines)


def parse_survey(survey_path: str) -> list[dict]:
    text = _read(survey_path)
    pat = re.compile(
        r"\*\*([^*]+?)\s*\(([^)]+?)(?:,\s*|\s+)(\d{4})\)"
        r"\.?\*\*\s*(?:[—–-]+\s*)?"
        r"([\s\S]*?)"
        r"URL[s]?:\s*(\S+)",
        re.MULTILINE,
    )
    out = []
    for m in pat.finditer(text):
        title    = m.group(1).strip().rstrip(".,")
        authors  = m.group(2).strip().rstrip(",")
        year     = m.group(3).strip()
        descr    = m.group(4).strip()
        url      = m.group(5).strip().rstrip(".,")
        out.append({
            "title":   title,
            "authors": authors,
            "year":    year,
            "descr":   descr,
            "url":     url,
        })
    return out


def _entries_from_survey() -> list[dict]:
    """Parse literature_survey.md and add per-entry derived fields needed
    by the bibitem renderer. Returns the structured list."""
    survey = os.path.join(_PAPER_DIR, "literature_survey.md")
    if not os.path.exists(survey):
        return []
    entries = parse_survey(survey)
    structured: list[dict] = []
    seen: set[str] = set()
    for e in entries:
        key = _bib_key(e["authors"], e["year"], e["title"])
        base = key
        i = 1
        while key in seen:
            i += 1
            key = f"{base}{chr(ord('a') + i - 1)}"
        seen.add(key)
        composite = e["authors"] + "\n" + e["descr"]
        etype = _infer_type(composite)
