def emit_paper(variant: str, run_id: str, entries: list[dict],
               dry_run: bool) -> None:
    print(f"[paper] variant={variant}, run-id={run_id}")
    print("[1/6] title_abstract.tex")
    update_title_abstract(variant, dry_run)
    print()
    print("[2/6] experiment.tex")
    update_experiment_tex(dry_run)
    print()
    print("[3/6] references.bib")
    generate_references_bib(_PAPER_DIR, entries, dry_run)
    print()
    print("[4/6] main.tex bibliography block")
    if entries:
        update_main_bibliography(entries, dry_run)
    else:
        print("  [skip] no entries — main.tex bibliography untouched")
    print()
    print("[5/6] paper/figures/")
    setup_figures(run_id, dry_run)
    print()
    print("[6/6] paper/tables/")
    setup_tables(dry_run)


