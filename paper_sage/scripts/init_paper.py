def _read_clean_abstract(variant: str) -> str:
    """Return the abstract body from paper/abstract_<variant>.tex, with
    surrounding ``\\begin{abstract}`` / ``\\end{abstract}`` removed."""
    path = os.path.join(_PAPER_DIR, f"abstract_{variant}.tex")
    if not os.path.exists(path):
        raise SystemExit(f"variant source not found: {path}")
    text = _read(path)
    m = re.search(r"\\begin\{abstract\}\s*([\s\S]*?)\s*\\end\{abstract\}", text)
    if m:
        return m.group(1).strip()
    m = re.search(r"\\abstract\s*\{", text)
    if m:
        close = _balanced_braces(text, m.end() - 1)
        if close > 0:
            return text[m.end():close].strip()
    raise SystemExit(f"could not find abstract block in {path}")


def _read_metadata() -> dict:
    """Parse title / authors / affils / keywords out of paper/tex/title_abstract.tex.
    Uses brace-balanced extraction so the (possibly corrupted) ``\\abstract{...}``
    body does not confuse the parser. Falls back to defaults if a field is
    missing or empty."""
    path = os.path.join(_PAPER_DIR, "tex", "title_abstract.tex")
    if not os.path.exists(path):
        return dict(title=_DEFAULT_TITLE, authors=_DEFAULT_AUTHORS,
                    affils=_DEFAULT_AFFILS, keywords=_DEFAULT_KEYWORDS)
    src = _read(path)

    def _arg_or_default(cmd: str, default: str) -> str:
        v = _extract_command_arg(src, cmd)
        if v is None or not v.strip():
            return default
        return v.strip()

    return dict(
        title    = _arg_or_default("title",    _DEFAULT_TITLE),
        authors  = _arg_or_default("author",   _DEFAULT_AUTHORS),
        affils   = _arg_or_default("affils",   _DEFAULT_AFFILS),
        keywords = _arg_or_default("keywords", _DEFAULT_KEYWORDS),
    )


# ─────────────────────────────────────────────────────────────────────
# Target=paper — existing behaviour, kept for backward compatibility
# ─────────────────────────────────────────────────────────────────────
def _extract_abstract_body(tex_path: str) -> str:
    text = _read(tex_path)
    m = re.search(r"\\begin\{abstract\}\s*([\s\S]*?)\s*\\end\{abstract\}", text)
    if m:
        return m.group(1).strip()
    m = re.search(r"\\abstract\s*\{\s*([\s\S]+?)\s*\}\s*\Z", text)
    if m:
        return m.group(1).strip()
    raise SystemExit(
        f"could not find an abstract block in {tex_path} — "
        "expected either \\begin{abstract}...\\end{abstract} or \\abstract{...}"
    )


def update_title_abstract(variant: str, dry_run: bool) -> None:
    src_path = os.path.join(_PAPER_DIR, f"abstract_{variant}.tex")
    if not os.path.exists(src_path):
        raise SystemExit(f"variant source not found: {src_path}")
    dst_path = os.path.join(_PAPER_DIR, "tex", "title_abstract.tex")
    if not os.path.exists(dst_path):
        raise SystemExit(f"target not found: {dst_path}")

    abstract_body = _extract_abstract_body(src_path)
    dst = _read(dst_path)

    # Brace-balanced replacement of \abstract{...}.
    m = re.search(r"\\abstract\s*\{", dst)
    if not m:
        raise SystemExit(f"no \\abstract{{...}} block in {dst_path}")
    open_idx = m.end() - 1
    close_idx = _balanced_braces(dst, open_idx)
    if close_idx < 0:
        raise SystemExit(f"unbalanced \\abstract{{...}} in {dst_path}")
    new = dst[: open_idx + 1] + "\n" + abstract_body + "\n" + dst[close_idx:]
    if new == dst:
        print(f"  ✓ title_abstract.tex already current (variant={variant})")
        return
    _backup_once(dst_path)
    _write(dst_path, new, dry_run)
    print(f"  ✓ title_abstract.tex updated (variant={variant}, "
          f"{len(abstract_body)} char abstract)")


