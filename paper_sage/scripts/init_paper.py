def _arxiv_id(url: str) -> str:
    """Extract the arXiv ID from a URL like https://arxiv.org/abs/2204.01691.
    Returns an empty string if the URL is not an arXiv URL."""
    m = re.search(r"arxiv\.org/(?:abs|pdf)/(\d{4}\.\d{4,5})", url or "")
    return m.group(1) if m else ""


def _format_authors_for_display(raw: str) -> str:
    """Convert 'Ahn and others' / 'Ahn et al' into 'Ahn et al.' for the
    rendered bibitem; multi-author lists are kept verbatim."""
    s = re.sub(r"\band others\b", "et al.", raw).strip()
    if not s.endswith(".") and s.lower().endswith("et al"):
        s += "."
    return s


def _bibitem_line(entry: dict) -> str:
    """Render one ``\\bibitem{key}`` block for a thebibliography list.

    The rendered entry includes (when available):
      - Authors with ``et al.`` rather than ``and others`` for IEEE style.
      - Real arXiv identifier (``arXiv:2204.01691``) parsed from the URL,
        so the reference is verifiable by ID and not just an URL.
      - Venue when known (inproceedings/article); falls back to
        ``arXiv preprint arXiv:NNNN.NNNNN`` for unpublished preprints.
    """
    key     = entry["key"]
    curated = _CURATED_REFS.get(key, {})
    title   = _latex_escape(curated.get("title") or entry["title"])
    authors = _latex_escape(curated.get("authors")
                            or _format_authors_for_display(entry["authors_bib"]))
    year    = curated.get("year")  or entry["year"]
    venue   = curated.get("venue") or entry["venue"] or ""
    etype   = curated.get("etype") or entry["etype"]
    arxiv   = _arxiv_id(entry.get("url", ""))
    if etype == "inproceedings":
        venue_str = (f"in \\textit{{Proc. of {venue}}}, "
                     if venue else "in \\textit{Proc. of the Conference}, ")
        body = f"{authors}, ``{title},'' {venue_str}{year}."
    elif etype == "article":
        venue_str = f"\\textit{{{venue}}}, " if venue else ""
        body = f"{authors}, ``{title},'' {venue_str}{year}."
    else:
        if arxiv:
            preprint_tag = f"arXiv preprint arXiv:{arxiv}"
        else:
            preprint_tag = venue or "arXiv preprint"
        body = f"{authors}, ``{title},'' {preprint_tag}, {year}."
    return f"\\bibitem{{{key}}}\n{body}"


def _bibliography_block(entries: list[dict]) -> str:
    """Return a full ``\\begin{thebibliography}{99} ... \\end{thebibliography}``
    block (including the begin/end lines)."""
    lines = [
        r"\begin{thebibliography}{99}",
        "% --- references --- regenerated by scripts/init_paper.py ---",
        "% To switch to BibTeX, replace this block with:",
        r"%     \bibliographystyle{IEEEtran}",
        r"%     \bibliography{references}",
        "% and run: pdflatex main && bibtex main && pdflatex main && pdflatex main",
        "",
    ]
    for e in entries:
        lines.append(_bibitem_line(e))
        lines.append("")
    lines.append(r"\end{thebibliography}")
    return "\n".join(lines).rstrip() + "\n"


