def _resolve_run(run_id: str) -> str:
    if run_id == "latest" or not run_id:
        candidates = sorted(
            (d for d in os.listdir(_FIG_ROOT) if os.path.isdir(os.path.join(_FIG_ROOT, d))),
            reverse=True,
        )
        if not candidates:
            raise SystemExit(f"no figure runs found in {_FIG_ROOT}")
        return candidates[0]
    if not os.path.isdir(os.path.join(_FIG_ROOT, run_id)):
        raise SystemExit(f"figure run not found: {os.path.join(_FIG_ROOT, run_id)}")
    return run_id


def _link_or_copy(src: str, dst: str) -> None:
    # Guard: src and dst resolve to the same real path. This happens when
    # the figures fallback aims paper/figures/ at itself for target=paper.
    # Replacing the file with a self-symlink would destroy it.
    try:
        if os.path.realpath(src) == os.path.realpath(dst):
            return
    except OSError:
        pass
    if os.path.islink(dst) or os.path.exists(dst):
        os.remove(dst)
    try:
        os.symlink(os.path.relpath(src, os.path.dirname(dst)), dst)
    except (OSError, NotImplementedError):
        shutil.copy2(src, dst)


def _link_figures_dir(run_id: str, dst_dir: str, dry_run: bool) -> None:
    """Populate ``dst_dir`` with .pdf and .eps files from
    ``figures/<run_id>/`` (symlinks where possible). Falls back to
    ``paper/figures/`` when no top-level figures/ tree exists yet."""
    if not os.path.isdir(_FIG_ROOT):
        fallback = os.path.join(_PAPER_DIR, "figures")
        if os.path.isdir(fallback):
            print(f"  [fallback] no figures/ root — sourcing from paper/figures/")
            src = fallback
            chosen = "paper/figures"
        else:
            print(f"  [skip] no figures/ root yet — run `make plots` first")
            return
    else:
        chosen = _resolve_run(run_id)
        src = os.path.join(_FIG_ROOT, chosen)
    # Same-dir guard: if src and dst are the same directory (the fallback
    # path with target=paper), nothing to do — the files are already there.
    if os.path.realpath(src) == os.path.realpath(dst_dir):
        print(f"  ✓ figures already in place at {dst_dir} (source = destination)")
        return
    print(f"  → figures source: {chosen}  →  {dst_dir}")
    if dry_run:
        n = sum(1 for f in os.listdir(src) if f.endswith((".pdf", ".eps")))
        print(f"  [dry-run] would link {n} .pdf/.eps files into {dst_dir}")
        return
    os.makedirs(dst_dir, exist_ok=True)
    n = 0
    for fn in sorted(os.listdir(src)):
        if not fn.endswith((".pdf", ".eps")):
            continue
        _link_or_copy(os.path.join(src, fn), os.path.join(dst_dir, fn))
        n += 1
    print(f"  ✓ {n} figure files linked into {dst_dir}")


