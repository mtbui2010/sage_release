def _cited_keys_in(*paths: str) -> set[str]:
    """Return the set of bib keys referenced via ``\\cite{...}`` in any
    of the given .tex files. Handles comma-separated key lists."""
    keys: set[str] = set()
    pat = re.compile(r"\\cite[a-z]*\{([^}]+)\}")
    for p in paths:
        if not os.path.exists(p):
            continue
        for m in pat.finditer(_read(p)):
            for k in m.group(1).split(","):
                k = k.strip()
                if k:
                    keys.add(k)
    return keys


def emit_iccas(variant: str, run_id: str, entries: list[dict],
               dry_run: bool) -> None:
    print(f"[ICCAS] variant={variant}, run-id={run_id}")
    meta = _read_metadata()
    abstract = _read_clean_abstract(variant)

    # 1. title_abstract.tex (command form)
    ta = _ICCAS_TITLE_ABSTRACT_TMPL
    ta = ta.replace("__TITLE__",    meta["title"])
    ta = ta.replace("__AUTHORS__",  meta["authors"])
    ta = ta.replace("__AFFILS__",   meta["affils"])
    ta = ta.replace("__ABSTRACT__", abstract)
    ta = ta.replace("__KEYWORDS__", meta["keywords"])
    _write(os.path.join(_ICCAS_DIR, "tex", "title_abstract.tex"), ta, dry_run)
    print(f"  ✓ ICCAS/tex/title_abstract.tex written ({len(abstract)} char abstract)")

    # 2. short_body.tex — copy from paper/
    short_src = os.path.join(_PAPER_DIR, "tex", "short_body.tex")
    if not os.path.exists(short_src):
        raise SystemExit(f"missing source: {short_src} — create it first")
    _copy_with_rewrite(short_src,
                       os.path.join(_ICCAS_DIR, "tex", "short_body.tex"),
                       dry_run, fig_dir="figs")

    # 3. main.tex — filter the bibliography to only entries cited in the
    #    ICCAS short body. The full survey lives unmodified in
    #    paper/literature_survey.md for the IJCAS journal version.
    cited = _cited_keys_in(short_src)
    iccas_entries = [e for e in entries if e["key"] in cited] if cited else entries
    print(f"  → bibliography filtered: {len(iccas_entries)}/{len(entries)} cited")
    bib_block = _bibliography_block(iccas_entries) if iccas_entries else \
                r"\begin{thebibliography}{99}" "\n" \
                "% no entries — populate paper/literature_survey.md\n" \
                r"\end{thebibliography}" "\n"
    main = _ICCAS_MAIN_TMPL.replace("__BIBLIOGRAPHY__", bib_block.rstrip())
    _write(os.path.join(_ICCAS_DIR, "main.tex"), main, dry_run)
    print(f"  ✓ ICCAS/main.tex written ({len(iccas_entries)} bib entries)")

    # 4. references.bib (for users who later switch to bibtex)
    generate_references_bib(_ICCAS_DIR, iccas_entries, dry_run)

    # 5. figures (figs/) and tables (tables/)
    _link_figures_dir(run_id, os.path.join(_ICCAS_DIR, "figs"), dry_run)
    _link_tables_dir(os.path.join(_ICCAS_DIR, "tables"), dry_run)


