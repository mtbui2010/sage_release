def main() -> int:
    p = argparse.ArgumentParser(
        description="Refresh paper/ICCAS/IJCAS artifacts from paper/ sources.")
    p.add_argument("--target",
                   choices=["paper", "iccas", "ijcas", "both"],
                   default="paper",
                   help="Which output folder(s) to emit. "
                        "'both' = iccas+ijcas (typical pre-submission).")
    p.add_argument("--variant",
                   choices=["completeness", "verifier", "llama"],
                   default="completeness",
                   help="Which abstract variant to use.")
    p.add_argument("--run-id", default="latest",
                   help="figures/<run-id>/ to source .pdf/.eps from")
    p.add_argument("--dry-run", action="store_true")
    args = p.parse_args()

    print("init_paper")
    print(f"  target    : {args.target}")
    print(f"  variant   : {args.variant}")
    print(f"  run-id    : {args.run_id}")
    print(f"  dry-run   : {args.dry_run}")
    print()

    # Parse the survey once — all targets share the entry list.
    entries = _entries_from_survey()
    if not entries:
        print("  [warn] no entries parsed from paper/literature_survey.md")

    if args.target == "paper":
        emit_paper(args.variant, args.run_id, entries, args.dry_run)
    elif args.target == "iccas":
        emit_iccas(args.variant, args.run_id, entries, args.dry_run)
    elif args.target == "ijcas":
        emit_ijcas(args.variant, args.run_id, entries, args.dry_run)
    elif args.target == "both":
        emit_iccas(args.variant, args.run_id, entries, args.dry_run)
        print()
        emit_ijcas(args.variant, args.run_id, entries, args.dry_run)

    print()
    print("done.")
    if not args.dry_run and not shutil.which("pdflatex"):
        print()
        print("note: pdflatex not on PATH — cannot test-compile here.")
        if args.target in ("iccas", "both"):
            print("  cd ICCAS && pdflatex main.tex")
        if args.target in ("ijcas", "both"):
            print("  cd IJCAS && pdflatex main.tex")
        if args.target == "paper":
            print("  cd paper && pdflatex main.tex")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
