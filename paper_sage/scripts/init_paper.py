def generate_references_bib(target_dir: str, entries: list[dict],
                            dry_run: bool) -> None:
    """Write references.bib to ``target_dir``. ``entries`` is the list
    returned by ``_entries_from_survey()``."""
    if not entries:
        print(f"  [skip] no survey entries — references.bib not written to {target_dir}")
        return
    bib_chunks: list[str] = [
        f"% {os.path.basename(target_dir)}/references.bib",
        "% Auto-generated by scripts/init_paper.py from paper/literature_survey.md.",
        "% To regenerate: python scripts/init_paper.py --target <paper|iccas|ijcas|both>",
        "",
    ]
    for e in entries:
        curated = _CURATED_REFS.get(e["key"], {})
        etype = curated.get("etype") or e["etype"]
        fields = {
            "title":  curated.get("title")   or e["title"],
            "author": curated.get("authors") or e["authors_bib"],
            "year":   curated.get("year")    or e["year"],
            "url":    e["url"],
        }
        venue = curated.get("venue") or e["venue"]
        if etype == "inproceedings":
            fields["booktitle"] = venue or "Proc. of the Conference"
        elif etype == "article":
            fields["journal"] = venue or "Journal"
        bib_chunks.append(_bibtex_entry(etype, e["key"], fields))
        bib_chunks.append("")
    bib_text = "\n".join(bib_chunks).rstrip() + "\n"
    dst = os.path.join(target_dir, "references.bib")
    if os.path.exists(dst) and _read(dst) == bib_text:
        print(f"  ✓ {dst} already current ({len(entries)} entries)")
        return
    _backup_once(dst)
    _write(dst, bib_text, dry_run)
    print(f"  ✓ {dst} written ({len(entries)} entries)")


