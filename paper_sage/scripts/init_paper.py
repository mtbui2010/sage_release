def _rewrite_fig_paths(text: str, *, from_dir: str = "figures", to_dir: str = "figs") -> str:
    """Replace ``figures/`` with ``figs/`` (or whatever the venue uses)
    inside ``\\includegraphics{...}`` arguments. Conservative: only
    rewrites occurrences inside includegraphics."""
    def _rewrite(match: re.Match) -> str:
        opts = match.group(1) or ""
        path = match.group(2)
        new_path = re.sub(rf"\b{re.escape(from_dir)}/", f"{to_dir}/", path)
        return f"\\includegraphics{opts}{{{new_path}}}"
    return re.sub(r"\\includegraphics(\[[^\]]*\])?\{([^}]+)\}", _rewrite, text)


def _copy_with_rewrite(src: str, dst: str, dry_run: bool,
                       *, fig_dir: str = "figs") -> None:
    """Copy a .tex source file rewriting figure paths from ``figures/``
    to ``<fig_dir>/``."""
    if not os.path.exists(src):
        print(f"  [warn] missing source: {src}")
        return
    text = _read(src)
    new_text = _rewrite_fig_paths(text, from_dir="figures", to_dir=fig_dir)
    _write(dst, new_text, dry_run)
    rewrote = "" if new_text == text else "  (figure paths rewritten)"
    print(f"  ✓ {os.path.relpath(dst, _PAPER_ROOT)}{rewrote}")


