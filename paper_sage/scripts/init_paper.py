def _ts() -> str:
    return time.strftime("%Y%m%d_%H%M%S")


def _backup_once(path: str) -> str | None:
    if not os.path.exists(path):
        return None
    bk = f"{path}.bak.init_paper"
    if not os.path.exists(bk):
        shutil.copy2(path, bk)
        return bk
    return None


def _read(path: str) -> str:
    with open(path, "r", encoding="utf-8") as f:
        return f.read()


def _write(path: str, content: str, dry_run: bool) -> None:
    if dry_run:
        print(f"  [dry-run] would write {path} ({len(content)} bytes)")
        return
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)


def _replace_between(src: str, open_pat: str, close_pat: str, new_inner: str) -> str | None:
    m = re.search(rf"({open_pat})([\s\S]*?)({close_pat})", src)
    if not m:
        return None
    return src[: m.start(2)] + new_inner + src[m.end(2) :]


def _balanced_braces(text: str, start_idx: int) -> int:
    """Given an index pointing AT the opening '{', return the index of the
    matching closing '}' (i.e. so text[start_idx+1:return_idx] is the body).
    Returns -1 if no match. Treats backslash-escapes (``\\{``, ``\\}``) as
    literal characters."""
    if start_idx >= len(text) or text[start_idx] != "{":
        return -1
    depth = 0
    i = start_idx
    while i < len(text):
        c = text[i]
        if c == "\\" and i + 1 < len(text):
            i += 2
            continue
        if c == "{":
            depth += 1
        elif c == "}":
            depth -= 1
            if depth == 0:
                return i
        i += 1
    return -1


def _extract_command_arg(text: str, cmd: str) -> str | None:
    """Return the body of the FIRST occurrence of ``\\cmd{...}``, using
    brace-balanced matching so the body itself can contain ``{...}``."""
    # \cmd, optional whitespace, then `{`. Stop at first match.
    m = re.search(rf"\\{re.escape(cmd)}\s*\{{", text)
    if not m:
        return None
    open_idx = m.end() - 1   # index of the `{`
    close_idx = _balanced_braces(text, open_idx)
    if close_idx < 0:
        return None
    return text[open_idx + 1 : close_idx].strip()


