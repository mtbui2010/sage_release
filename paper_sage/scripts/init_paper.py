def _plainify_authors(authors_tex: str) -> str:
    """Convert ICCAS-style ``First A. Author${}^{1}$ and ...`` into a plain
    string suitable for the IJCAS ``\\author{...}`` argument (which expects
    a comma- and ``and``-separated list without affiliation superscripts)."""
    txt = re.sub(r"\$\{?\}?\^\{[^{}]*\}\$", "", authors_tex)   # ${}^{1}$
    txt = re.sub(r"\$\^\{[^{}]*\}\$", "", txt)                  # $^{1}$
    txt = re.sub(r"\\textsuperscript\{[^{}]*\}", "", txt)
    txt = re.sub(r"\s{2,}", " ", txt).strip()
    return txt


def _plainify_affils(affils_tex: str) -> str:
    """Flatten ICCAS \\affils{...} into a single paragraph suitable for
    IJCAS' \\makeAuthorInformation{...} block."""
    txt = re.sub(r"\\\\", "; ", affils_tex)
    txt = re.sub(r"\$\{?\}?\^\{[^{}]*\}\$", "", txt)
    txt = re.sub(r"\$\^\{[^{}]*\}\$", "", txt)
    txt = re.sub(r"\{\\small\s*([^}]*)\}", r"\1", txt)
    # ``\\`` in ICCAS marks a line break, often preceded by ``,`` for the
    # multi-line affil. Flatten those into a single ``;`` so the IJCAS
    # one-paragraph layout doesn't end up with stray ``, ;`` punctuation.
    txt = re.sub(r",\s*;", ";", txt)
    txt = re.sub(r"\s+;\s+", "; ", txt)
    txt = re.sub(r";\s*;", ";", txt)
    txt = re.sub(r"\s{2,}", " ", txt).strip()
    return txt


def emit_ijcas(variant: str, run_id: str, entries: list[dict],
               dry_run: bool) -> None:
    print(f"[IJCAS] variant={variant}, run-id={run_id}")
    meta = _read_metadata()
    abstract = _read_clean_abstract(variant)
    year = time.strftime("%Y")

    # 1. title_abstract.tex (env form)
    ta = _IJCAS_TITLE_ABSTRACT_TMPL
    ta = ta.replace("__TITLE__",         meta["title"])
    ta = ta.replace("__AUTHORS_PLAIN__", _plainify_authors(meta["authors"]))
    ta = ta.replace("__AFFILS_PLAIN__",  _plainify_affils(meta["affils"]))
    ta = ta.replace("__ABSTRACT__",      abstract)
    ta = ta.replace("__KEYWORDS__",      meta["keywords"])
    ta = ta.replace("__YEAR__",          year)
    _write(os.path.join(_IJCAS_DIR, "tex", "title_abstract.tex"), ta, dry_run)
    print(f"  ✓ IJCAS/tex/title_abstract.tex written ({len(abstract)} char abstract)")

    # 2. Copy section files with figure-path rewriting (figs/).
    section_files = [
        "intro_related.tex",
        "method.tex",
        "experiment.tex",
        "conclusion.tex",
        "acknowledgement.tex",
        "biography.tex",
    ]
    for fn in section_files:
        src = os.path.join(_PAPER_DIR, "tex", fn)
        dst = os.path.join(_IJCAS_DIR, "tex", fn)
        _copy_with_rewrite(src, dst, dry_run, fig_dir="figs")

    # 3. main.tex — filter bibliography to only refs actually cited in
    #    the IJCAS body sources. The full survey lives unmodified in
    #    paper/literature_survey.md for context.
    cited = _cited_keys_in(*(
        os.path.join(_PAPER_DIR, "tex", fn)
        for fn in ("intro_related.tex", "method.tex", "experiment.tex",
                   "conclusion.tex", "acknowledgement.tex")
    ))
    ijcas_entries = [e for e in entries if e["key"] in cited] if cited else entries
    print(f"  → bibliography filtered: {len(ijcas_entries)}/{len(entries)} cited")
    bib_block = _bibliography_block(ijcas_entries) if ijcas_entries else \
                r"\begin{thebibliography}{99}" "\n" \
                "% no entries — populate paper/literature_survey.md\n" \
                r"\end{thebibliography}" "\n"
    main = _IJCAS_MAIN_TMPL.replace("__BIBLIOGRAPHY__", bib_block.rstrip())
    main = main.replace("__YEAR__", year)
    _write(os.path.join(_IJCAS_DIR, "main.tex"), main, dry_run)
    print(f"  ✓ IJCAS/main.tex written ({len(ijcas_entries)} bib entries)")

    # 4. references.bib
    generate_references_bib(_IJCAS_DIR, ijcas_entries, dry_run)

    # 5. figures (figs/), tables (tables/), portraits (portraits/)
    _link_figures_dir(run_id, os.path.join(_IJCAS_DIR, "figs"), dry_run)
    _link_tables_dir(os.path.join(_IJCAS_DIR, "tables"), dry_run)
    _link_portraits_dir(os.path.join(_IJCAS_DIR, "portraits"), dry_run)


